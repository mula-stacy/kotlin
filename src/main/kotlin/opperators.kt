    println(num1/num2)
    println(num1%num2)//Modulus(remainder)

    //Assignment Operators
    var x=6
    x+=5
    println(x)

    var y=10
    y-=3
    println(y)

    var z=10
    z*=3
    println(z)

    var m=9
    m/=3
    println(m)

    var e=50
    e%=3
    println(e)
    //Comparison Operators
    var b=20
    println(b==2)
    println(b<2)
    println(b>2)
    println(b<=2)
    println(b>=2)

    //Logic Operators
    var  mylogic=80
    println(mylogic<50 && mylogic==80)//&&(And)
